using std::string;

static inline int asciiToLower(int c)
{
	if (c >= 'A' && c <= 'Z') return c - 'A' + 'a';
	return c;
}

int getCountryByName(const string &str)
{
	string::size_type len = str.find(',');
	if (len == string::npos) len = str.length();
	if (len > MAX_WORD_LENGTH || len < MIN_WORD_LENGTH) return COUNTRY_NONE;
	int key = hash(str.c_str(), len);
	if (key > MAX_HASH_VALUE || key < 0) return COUNTRY_NONE;
	const char *s = wordList[key];
	if ((((unsigned char) str[0] ^ (unsigned char) *s) & ~32)) return COUNTRY_NONE;
	size_t wordLen = strlen(s);
	if (wordLen != len) return COUNTRY_NONE;
	int result = wordListValues[key];
	for (size_t i = 0; i < wordLen; i++)
		if (asciiToLower(str[i]) != s[i])
			return COUNTRY_NONE;
	// Check aliases
	switch (result)
	{
		case COUNTRY_RUSSIA: result = COUNTRY_RUSSIAN_FEDERATION; break;
		case COUNTRY_EUROPEAN_UNION: result = COUNTRY_EUROPE; break;
	}
	return result;
}
